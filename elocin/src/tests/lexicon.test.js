// Correction 8 — eight curriculum packs, no leak into core behaviour.
// ---------------------------------------------------------------------------
describe('Curriculum packs (opt-in, no leak)', () => {
  const cases = [
    ['montessori', 'Ava used the golden beads to count', 'kinesthetic'],
    ['reggio', 'Ava explored the loose parts at the atelier', 'kinesthetic'],
    ['waldorf', 'the class did eurythmy this morning', 'gestural'],
    ['highscope', 'we did plan-do-review before centers', 'chunking'],
    ['creative_curriculum', 'used an intentional teaching card', 'modeling'],
    ['tools_of_the_mind', 'Ava made a play plan first', 'visual'],
    ['structured_literacy', 'we used elkonin boxes to segment', 'visual'],
    ['structured_math', 'a number talk about making ten', 'whole_group']
  ]
  for (const [pack, sentence, key] of cases) {
    test(`${pack}: inert without pack, active with it`, () => {
      assert.ok(!methodKeys(parseObservation(sentence, { roster: ['Ava'] })).includes(key), `${key} leaked without pack`)
      assert.ok(methodKeys(parseObservation(sentence, { roster: ['Ava'], packs: [pack] })).includes(key), `${key} missing with pack`)
    })
  }
  test('unknown pack names are ignored', () => {
    assert.doesNotThrow(() => parseObservation('Ava read a book', { roster: ['Ava'], packs: ['nope'] }))
  })
})

describe('Additive parsed_json fields + invariants', () => {
  test('methods carry source, output stamps lexicon version, suggestions present', () => {
    const p = parseObservation('Ava used picture cards to read the passage', { roster: ['Ava'] })
    assert.equal(p.lexicon, '1.3')
    assert.ok(p.methods.every((m) => m.source === 'auto'))
    assert.ok(Array.isArray(p.suggestions.skills) && Array.isArray(p.suggestions.methods))
  })
  test('confidenceScore stays within the locked 0..4 range', () => {
    for (const n of ['Ava mastered counting to 20 with unifix cubes, 5/5 first time', 'ok', 'cant read any big words with a flash card']) {
      const s = parseObservation(n, { roster: ['Ava'] }).confidenceScore
      assert.ok(s >= 0 && s <= 4, `${s} out of range for "${n}"`)
    }
  })
})
