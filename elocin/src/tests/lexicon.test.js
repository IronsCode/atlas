import { test, describe } from 'node:test'
import assert from 'node:assert/strict'
import { readFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { parseObservation } from '../engine/index.js'
import { normalizeExact, expandAbbreviations, stemToken } from '../core/rules/normalize.js'

const methodKeys = (p) => p.methods.map((m) => m.key)
const suggMethodKeys = (p) => p.suggestions.methods.map((m) => m.key)
const HERE = dirname(fileURLToPath(import.meta.url))

// ---------------------------------------------------------------------------
// Correction 1 (highest priority) — seed-note regression lock.
// Every note in the demo seed must keep parsing exactly as committed. The
// fixture is regenerated by scripts/gen_seed.mjs alongside the seed; if an
// engine change alters any seed parse, this fails and must be done knowingly.
// ---------------------------------------------------------------------------
describe('Seed-parse regression lock', () => {
  const fixture = JSON.parse(readFileSync(join(HERE, 'fixtures', 'seed_parses.json'), 'utf8'))
  test(`fixture present with ${fixture.notes.length} notes`, () => {
    assert.ok(fixture.notes.length >= 30)
  })
  for (const { raw, parsed } of fixture.notes) {
    test(`stable parse: "${raw.slice(0, 48)}"`, () => {
      // seed org enables no packs
      assert.deepEqual(parseObservation(raw, { context: parsed.context, roster: parsed.students }), parsed)
    })
  }
})

// ---------------------------------------------------------------------------
// Correction 5 — precedence / negation / no double-counting.
// ---------------------------------------------------------------------------
describe('Precedence rules', () => {
  test('"without" AFTER the trigger does not negate; note is mixed (Emma seed note)', () => {
    const p = parseObservation('Emma struggled with CVC blending but got 3/5 with picture cards vs 1/5 without.', { roster: ['Emma'] })
    const visual = p.methods.find((m) => m.key === 'visual')
    assert.ok(visual, 'visual should be detected')
    assert.equal(visual.negated, false, '"without" trails the trigger → not negated')
    assert.equal(p.outcome, 'mixed')
  })
  test('"without" directly before a trigger negates it', () => {
    const p = parseObservation('Ava read the passage without picture cards today', { roster: ['Ava'] })
    assert.equal(p.methods.find((m) => m.key === 'visual').negated, true)
  })
  test('negation only fires within 4 words before the trigger', () => {
    const near = parseObservation('Ava did not use the picture cards', { roster: ['Ava'] })
    assert.equal(near.methods.find((m) => m.key === 'visual').negated, true)
    const far = parseObservation('Ava was not here yesterday but today used the picture cards well', { roster: ['Ava'] })
    assert.equal(far.methods.find((m) => m.key === 'visual').negated, false)
  })
  test('a negation token is not double-counted as a negative outcome', () => {
    // "not" negates the method; the only outcome word is "independently" (positive)
    const p = parseObservation('Ava did not use picture cards but worked independently', { roster: ['Ava'] })
    assert.equal(p.methods.find((m) => m.key === 'visual').negated, true)
    assert.equal(p.outcome, 'positive')
  })
  test('"independently" plays a dual role: independent method AND positive outcome', () => {
    const p = parseObservation('Ava worked independently on her counting', { roster: ['Ava'] })
    assert.ok(methodKeys(p).includes('independent'))
    assert.equal(p.outcome, 'positive')
  })
})

// ---------------------------------------------------------------------------
// Correction 3 — per-trigger tiers.
// ---------------------------------------------------------------------------
describe('Trigger tiers', () => {
  test('single common noun (cube) is MEDIUM → suggestion, not auto', () => {
    const p = parseObservation('Ava counted with a cube', { roster: ['Ava'] })
    assert.ok(!methodKeys(p).includes('kinesthetic'))
    assert.ok(suggMethodKeys(p).includes('kinesthetic'))
  })
  test('unambiguous term (unifix) is HIGH → auto-applied', () => {
    const p = parseObservation('Ava built the number with unifix cubes', { roster: ['Ava'] })
    assert.ok(methodKeys(p).includes('kinesthetic'))
  })
  test('number line is reclassified from counting skill to the visual method', () => {
    const p = parseObservation('Ava used the number line to add', { roster: ['Ava'] })
    assert.ok(methodKeys(p).includes('visual'))
    assert.ok(!p.skills.includes('counting'))
  })
  test('one to one is its own skill (not a counting trigger)', () => {
    const p = parseObservation('Ava used one to one correspondence to count the bears', { roster: ['Ava'] })
    assert.ok(p.skills.includes('one_to_one'))
  })
})

