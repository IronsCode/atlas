describe('Structural patterns', () => {
  test('attempts pattern → repetition method', () => {
    assert.ok(methodKeys(parseObservation('Ava blended it on the 3rd try', { roster: ['Ava'] })).includes('repetition'))
  })
  test('prompt-count pattern → verbal_prompt method', () => {
    assert.ok(methodKeys(parseObservation('Ava lined up after 2 prompts', { roster: ['Ava'] })).includes('verbal_prompt'))
  })
  test('score pattern contributes a positive outcome', () => {
    assert.equal(parseObservation('Ava got 4 of 5 correct', { roster: ['Ava'] }).outcome, 'positive')
  })
})

describe('Method taxonomy — every one of the 16 keys matches a realistic note', () => {
  const cases = {
    visual:          'Ava used picture cards to sort the sounds today',
    kinesthetic:     'Ava used unifix cubes and manipulatives to build the number',
    gestural:        'Ava used a hand signal and finger tracing for each sound',
    verbal_prompt:   'Ava needed a verbal prompt to begin the task',
    physical_assist: 'Ava needed hand over hand to guide the pencil',
    modeling:        'the teacher modeled the strategy before she tried it',
    one_on_one:      'Ava worked one on one at the teacher table',
    small_group:     'Ava worked in a small group during literacy',
    whole_group:     'during whole group carpet time Ava listened well',
    partner:         'Ava worked with a partner during turn and talk',
    independent:     'Ava did independent practice on her own quietly',
    repetition:      'lots of repetition helped Ava with her sound drill',
    music_chant:     'Ava sang a counting song with the class',
    sensory:         'Ava used a wiggle seat and a sensory break to focus',
    reinforcement:   'Ava earned a spot on her token board today',
    chunking:        'we broke it down step by step for Ava'
  }
  for (const [key, sentence] of Object.entries(cases)) {
    test(`method '${key}' detected`, () => {
      assert.ok(methodKeys(parseObservation(sentence, { roster: ['Ava'] })).includes(key),
        `expected '${key}', got ${JSON.stringify(methodKeys(parseObservation(sentence, { roster: ['Ava'] })))}`)
    })
  }
  test('old group/guided keys are gone; group work → small_group', () => {
    const p = parseObservation('Ava did well in group work today', { roster: ['Ava'] })
    assert.ok(methodKeys(p).includes('small_group'))
    assert.ok(!methodKeys(p).includes('group'))
  })
})

describe('Normalization + stemming', () => {
  test('lowercases, strips apostrophes, keeps : and -', () => {
    assert.equal(normalizeExact("  Can’t work 1:1 or 1-on-1  "), 'cant work 1:1 or 1-on-1')
  })
  test('stemToken strips regular inflections', () => {
    assert.equal(stemToken('blending'), 'blend')
    assert.equal(stemToken('cards'), 'card')
    assert.equal(stemToken('tries'), 'try')
    assert.equal(stemToken('running'), 'run')
  })
  test('1:1 / 1-on-1 both map to one_on_one (normalization preserves : and -)', () => {
    assert.ok(methodKeys(parseObservation('Ava worked 1:1 then 1-on-1 with me', { roster: ['Ava'] })).includes('one_on_one'))
  })
  test('a stemmed-only skill match is a suggestion, not auto', () => {
    const p = parseObservation('Ava decoded three of the words', { roster: ['Ava'] })
    assert.ok(p.suggestions.skills.includes('phonics'))
    assert.ok(!p.skills.includes('phonics'))
  })
})

describe('Abbreviations', () => {
  test('expandAbbreviations maps multi-word forms', () => {
    assert.equal(expandAbbreviations('num line in sm grp', { 'num line': 'number line', 'sm grp': 'small group' }), 'number line in small group')
  })
  test('an abbreviation-only match is a suggestion', () => {
    const p = parseObservation('Ava worked in a sm grp during maths', { roster: ['Ava'] })
    assert.ok(suggMethodKeys(p).includes('small_group'))
    assert.ok(!methodKeys(p).includes('small_group'))
  })
})

